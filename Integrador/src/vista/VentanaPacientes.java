    private void limpiar() {
        // limpio las cajas de texto y labels
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtTelefono.setText("");
        this.txtDireccion.setText("");
        this.txtFechanacimiento.setText("");
        this.txtDni.setText("");

        this.labelId.setText("");
        this.PacienteLista.setListData(this.Controlador.listarPaciente().toArray());
        //DESELECCIONO LA LISTA
        this.PacienteLista.clearSelection();

    }

    public int validarDni() {
        try {
            Integer.parseInt(this.txtDni.getText());
            //JOptionPane.showMessageDialog(null, "DATOS VALIDOS");
            this.txtDni.setBorder(BorderFactory.createLineBorder(Color.GREEN, 1));
            return 0; //ok
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "EL DNI DEL PACIENTE DEBE DE SER NUMERICO");
            this.txtDni.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return 1; //no ok
        }
    }


