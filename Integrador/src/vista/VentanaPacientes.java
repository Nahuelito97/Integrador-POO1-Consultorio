    private void PacienteListaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_PacienteListaValueChanged

        //VERIFICAMOS QUE LA LISTA TENGA UN ITEM SELECCIONADO
        if (!this.PacienteLista.isSelectionEmpty()) {
            //obtener la fila seleccionada
            Paciente pacie = (Paciente) this.PacienteLista.getSelectedValue();
            this.pa = (Paciente) this.PacienteLista.getSelectedValue();
            
            //LLEVAMOS LOS ATRIBUTOS A LAS CAJAS DE TEXTO
            this.labelId.setText(String.valueOf(pacie.getIdpac()));
            this.txtApellido.setText(pacie.getApellidos());
            this.txtNombre.setText(pacie.getNombres());
            this.txtDireccion.setText(pacie.getDireccion());
            this.txtDni.setText(pacie.getDni());
            this.txtTelefono.setText(pacie.getTelefono());

            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
            String fecha;
            this.txtFechanacimiento.setText(fecha = formatoFecha.format(pacie.getFechanacimiento()));

        }

    }//GEN-LAST:event_PacienteListaValueChanged

