    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        if (validarDni() == 0) {
            if (!this.PacienteLista.isSelectionEmpty()) {
                Paciente p = (Paciente) this.PacienteLista.getSelectedValue();
                this.Controlador.editarPaciente(p, this.txtDireccion.getText(), this.txtTelefono.getText());
                JOptionPane.showMessageDialog(null, "EL PACIENTE FUE MODIFICADO CON EXITO", "EXITO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                this.Controlador.agregarPaciente(this.txtNombre.getText(), this.txtApellido.getText(),
                        this.txtDni.getText(), this.txtDireccion.getText(), this.txtTelefono.getText(),
                        this.txtFechanacimiento.getText());
                JOptionPane.showMessageDialog(null, "EL PACIENTE FUE AGREGADO CON EXITO", "EXITO", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "COMPLETE LOS CAMPOS NECESARIOS", "ERROR", HEIGHT);
        }
        limpiar();//LIMPIO LA VENTANA PACIENTES
    }//GEN-LAST:event_BotonGuardarActionPerformed

